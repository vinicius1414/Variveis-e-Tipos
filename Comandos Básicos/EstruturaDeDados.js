//Javascript é uma linguagem de tipagem dinâmica! ou seja, não é preciso especificar
//o tipo da variável antes de declarar. EXEMPLO:

let um = "um";
// 'um' é uma String, pois está entre aspas

um = 1;
/* 
um deixa de ser uma String, e se torna um Int, e não há problemas nisso, pois em javascript é possível 
alterar o tipo da variável ao atribuir um novo valor para ela.
*/
